package JAVA_EXC;

public class exc11 {
    public static void main(String[]args) {
        double n = 7.5;
        double c = 2 * Math.PI * n;
        double a = Math.PI * n * n;
        System.out.println("Radius = "+n);
        System.out.println("Perimeter is = "+c);
        System.out.println("Area is = "+a);
    }
}

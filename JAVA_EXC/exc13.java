package JAVA_EXC;

public class exc13 {
    public static void main(String[]args) {
        double w = 5.6;
        double h = 8.5;
        double a = w * h;
        double p = 2 * (w + h);
        System.out.println("Width = "+ w + " Height = "+ h);
        System.out.println("Area is 5.6 * 8.5 = "+ a);
        System.out.println("Perimeter is 2 * (5.6 + 8.5) = "+ p);
    }
}
